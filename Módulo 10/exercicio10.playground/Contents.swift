// Descrever mais de uma diferença entre Struct e Class
/* Resposta:
 
 A principal diferença entre as duas é que classes são reference types, e as structs são values
 types.
 
 As classes podem herdar de outras classes, já structs não herdam nada.
 
 Classes possuem o conceito deinit(), que é executado quando uma instancia de uma classe é destruída.
 
 Classes podem também ter uma ou mais referencias para uma unica instancia.
 
 */

